LIVE_UPGRADE_REQUIRE_REPORTED_AVAILABLE=${PRL_LIVE_UPGRADE_REQUIRE_REPORTED_AVAILABLE:-1}
NO_CREDITS_BACKOFF_SECONDS=${PRL_NO_CREDITS_BACKOFF_SECONDS:-120}
NO_CREDITS_TARGET_REFRESH_SECONDS=${PRL_NO_CREDITS_TARGET_REFRESH_SECONDS:-300}
GUARD_POLL_SECONDS=${PRL_GUARD_POLL_SECONDS:-20}
NEGATIVE_SLOT_GRACE_SECONDS=${PRL_NEGATIVE_SLOT_GRACE_SECONDS:-900}
ORG_BALANCE_MAX_AGE_SECONDS=10800
FILL_FIXED_DECISION_PRICE_USD=${PRL_FILL_FIXED_DECISION_PRICE_USD:-0.64}
OPTIMIZE_FIXED_DECISION_PRICE_USD=${PRL_OPTIMIZE_FIXED_DECISION_PRICE_USD:-0.62}
