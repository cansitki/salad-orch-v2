  DECISION_PRICE_CAP_USD=${PRL_DECISION_PRICE_CAP_USD:-0}
fi
FALLBACK_DECISION_PRICE_USD=${PRL_NOHASH_FALLBACK_PRICE:-0.62}
if [[ "$FIXED_DECISION_PRICE_USD" != "0" && "$FIXED_DECISION_PRICE_USD" != "0.0" && "$FIXED_DECISION_PRICE_USD" != "0.00" ]]; then
  FALLBACK_DECISION_PRICE_USD=$FIXED_DECISION_PRICE_USD
fi

"$PYTHON" "$PREFLIGHT" --decision-price "$FIXED_DECISION_PRICE_USD" --min-profitable-profiles 1

slots() {
  local prefix=$1
  local out=""
  local i
  for i in $(seq -w 1 10); do
    if [[ -n "$out" ]]; then
      out+=","
    fi
    out+="${prefix}-${i}"
  done
  printf '%s' "$out"
}

api_key_env_for_org() {
  local org=$1
  local key_env=$DEFAULT_API_KEY_ENV
  local upper_org
  local per_org_var
  upper_org=$(printf '%s' "$org" | tr '[:lower:]-' '[:upper:]_')
  per_org_var="PRL_WATCH_API_KEY_ENV_${upper_org}"
  if [[ -n "${!per_org_var+x}" && -n "${!per_org_var}" ]]; then
    key_env=${!per_org_var}
  fi
  printf '%s' "$key_env"
}

global_pool_prefixes() {
  local out=""
  local org
  IFS=',' read -ra ORGS <<< "$COMMON_ORGS"
  for org in "${ORGS[@]}"; do
    org=${org//[[:space:]]/}
    [[ -n "$org" ]] || continue
    if [[ -n "$out" ]]; then
      out+=","
    fi
    out+="${org}-prl-${org}"
  done
  printf '%s' "$out"
}

GLOBAL_POOL_WORKER_PREFIXES=${PRL_GLOBAL_POOL_WORKER_PREFIXES:-$(global_pool_prefixes)}

restart_tmux() {
  local session=$1
  shift
  tmux has-session -t "$session" 2>/dev/null && tmux kill-session -t "$session"
  tmux new-session -d -s "$session" "$*"
}

