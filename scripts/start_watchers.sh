#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR=$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)
REPO_ROOT=$(cd -- "$SCRIPT_DIR/.." && pwd)
ENV_FILE=${SALAD_PRL_ENV:-"$REPO_ROOT/.env"}
if [[ -f "$ENV_FILE" ]]; then
  while IFS= read -r line || [[ -n "$line" ]]; do
    [[ -n "$line" ]] || continue
    [[ "$line" =~ ^[[:space:]]*# ]] && continue
    [[ "$line" == *"="* ]] || continue
    key=${line%%=*}
    value=${line#*=}
    key=${key//[[:space:]]/}
    [[ "$key" =~ ^[A-Za-z_][A-Za-z0-9_]*$ ]] || continue
    if [[ -z "${!key+x}" ]]; then
      value=${value%\"}
      value=${value#\"}
      value=${value%\'}
      value=${value#\'}
      export "$key=$value"
    fi
  done < "$ENV_FILE"
fi
STATE_DIR=${SALAD_PRL_STATE_DIR:-"$REPO_ROOT/state"}
LOG_DIR="$STATE_DIR/logs"
mkdir -p "$LOG_DIR"
STOPPED_SLOT_STATE_PATH=${PRL_STOPPED_SLOT_STATE_PATH:-"$STATE_DIR/prl_stopped_slots.json"}

SCRIPT="$SCRIPT_DIR/salad_prl_watch.py"
GUARD="$SCRIPT_DIR/salad_prl_guard.py"
PREFLIGHT="$SCRIPT_DIR/salad_prl_preflight.py"
PYTHON=${SALAD_PRL_PYTHON:-"$REPO_ROOT/.venv/bin/python"}
if [[ ! -x "$PYTHON" ]]; then
  PYTHON=python3
fi
KRY1_ORG_ID=kry1
COMMON_ORGS=${PRL_FLEET_ORGS:-kray,kry1,kray2,kray3}
DEFAULT_API_KEY_ENV=${PRL_WATCH_DEFAULT_API_KEY_ENV:-SALAD_API_KEY}
FLEET_MODE=${PRL_FLEET_MODE:-fill}
AGGRESSIVE_DISCOVERY=${PRL_AGGRESSIVE_DISCOVERY:-1}
TARGET_OFFSET=${PRL_WATCH_TARGET_OFFSET:-0}
ALLOWED_PRIORITIES=${PRL_WATCH_ALLOWED_PRIORITIES:-batch,low}
BLOCKED_PROFILES=${PRL_WATCH_BLOCKED_PROFILES:-5070:low,5090:low}
COORDINATED_TARGET_OFFSETS=${PRL_WATCH_COORDINATED_TARGET_OFFSETS:-1}
if [[ "$AGGRESSIVE_DISCOVERY" =~ ^(1|true|yes)$ ]]; then
  POLL_SECONDS=${PRL_POLL_SECONDS:-15}
  ALLOCATING_ROTATE_SECONDS=${PRL_ALLOCATING_ROTATE_SECONDS:-900}
  STOPPED_RESTART_COOLDOWN_SECONDS=${PRL_STOPPED_RESTART_COOLDOWN_SECONDS:-60}
  EMPTY_STUCK_NON_LIVE_SECONDS=${PRL_EMPTY_STUCK_NON_LIVE_SECONDS:-3600}
  NO_GPU_SLEEP_AFTER_SECONDS=${PRL_NO_GPU_SLEEP_AFTER_SECONDS:-0}
  NO_GPU_SLEEP_SECONDS=${PRL_NO_GPU_SLEEP_SECONDS:-0}
  ZERO_AVAILABLE_PROBE_BUDGET=${PRL_WATCH_ZERO_AVAILABLE_PROBE_BUDGET:-10}
  ZERO_AVAILABLE_PROBE_MAX_LIVE_WORKERS=${PRL_WATCH_ZERO_AVAILABLE_PROBE_MAX_LIVE_WORKERS:-30}
  STUCK_NON_LIVE_MAX_ACTIONS=${PRL_STUCK_NON_LIVE_MAX_ACTIONS:-1}
else
  POLL_SECONDS=${PRL_POLL_SECONDS:-30}
  ALLOCATING_ROTATE_SECONDS=${PRL_ALLOCATING_ROTATE_SECONDS:-900}
  STOPPED_RESTART_COOLDOWN_SECONDS=${PRL_STOPPED_RESTART_COOLDOWN_SECONDS:-120}
  EMPTY_STUCK_NON_LIVE_SECONDS=${PRL_EMPTY_STUCK_NON_LIVE_SECONDS:-3600}
  NO_GPU_SLEEP_AFTER_SECONDS=${PRL_NO_GPU_SLEEP_AFTER_SECONDS:-300}
  NO_GPU_SLEEP_SECONDS=${PRL_NO_GPU_SLEEP_SECONDS:-120}
  ZERO_AVAILABLE_PROBE_BUDGET=${PRL_WATCH_ZERO_AVAILABLE_PROBE_BUDGET:-0}
  ZERO_AVAILABLE_PROBE_MAX_LIVE_WORKERS=${PRL_WATCH_ZERO_AVAILABLE_PROBE_MAX_LIVE_WORKERS:-0}
  STUCK_NON_LIVE_MAX_ACTIONS=${PRL_STUCK_NON_LIVE_MAX_ACTIONS:-1}
fi
STUCK_NON_LIVE_MIN_ACTIVE_SLOTS=${PRL_STUCK_NON_LIVE_MIN_ACTIVE_SLOTS:-28}
HTTP_TIMEOUT_SECONDS=15
SNAPSHOT_HTTP_TIMEOUT_SECONDS=${PRL_SNAPSHOT_HTTP_TIMEOUT_SECONDS:-8}
SNAPSHOT_HTTP_ATTEMPTS=${PRL_SNAPSHOT_HTTP_ATTEMPTS:-3}
NOHASH_GRACE_SECONDS=${PRL_NOHASH_GRACE_SECONDS:-900}
STALE_WORKER_GRACE_SECONDS=${PRL_STALE_WORKER_GRACE_SECONDS:-900}
UNDERPERFORM_GRACE_SECONDS=120
UNDERPERFORM_RATIO=0.85
UNDERPERFORM_MIN_DEFICIT_TH=10
STUCK_NON_LIVE_TICK_BUDGET_SECONDS=${PRL_STUCK_NON_LIVE_TICK_BUDGET_SECONDS:-20}
STUCK_NON_LIVE_SECONDS=${PRL_STUCK_NON_LIVE_SECONDS:-3600}
STUCK_RUNNING_ZERO_DEFER_SECONDS=${PRL_STUCK_RUNNING_ZERO_DEFER_SECONDS:-3600}
GLOBAL_POOL_MIN_FRESH_WORKERS=${PRL_GLOBAL_POOL_MIN_FRESH_WORKERS:-8}
ORG_COUNT=0
IFS=',' read -ra COUNT_ORGS <<< "$COMMON_ORGS"
for org in "${COUNT_ORGS[@]}"; do
  org=${org//[[:space:]]/}
  [[ -n "$org" ]] && ORG_COUNT=$((ORG_COUNT + 1))
done
LIVE_UPGRADE_GLOBAL_MIN_FRESH_WORKERS=${PRL_LIVE_UPGRADE_GLOBAL_MIN_FRESH_WORKERS:-$((ORG_COUNT * 10 - 3))}
if (( LIVE_UPGRADE_GLOBAL_MIN_FRESH_WORKERS < 0 )); then
  LIVE_UPGRADE_GLOBAL_MIN_FRESH_WORKERS=0
fi
PROFILE_MISMATCH_GRACE_SECONDS=120
ALLOCATING_RETARGET_AVAILABLE_SECONDS=${PRL_ALLOCATING_RETARGET_AVAILABLE_SECONDS:-180}
LOW_LIVE_MIN_LIVE_WORKERS=${PRL_LOW_LIVE_MIN_LIVE_WORKERS:-8}
LOW_LIVE_ALLOCATING_RETARGET_AVAILABLE_SECONDS=${PRL_LOW_LIVE_ALLOCATING_RETARGET_AVAILABLE_SECONDS:-$ALLOCATING_ROTATE_SECONDS}
CREATE_PROGRESS_SECONDS=900
EMPTY_CREATING_SECONDS=600
UPGRADE_TO_BEST_SECONDS=999999
LIVE_UPGRADE_INTERVAL_SECONDS=${PRL_LIVE_UPGRADE_INTERVAL_SECONDS:-10800}
LIVE_UPGRADE_MIN_PROFIT_DAY=${PRL_LIVE_UPGRADE_MIN_PROFIT_USD_DAY:-0.5}
LIVE_UPGRADE_MIN_TH_DELTA=${PRL_LIVE_UPGRADE_MIN_TH_DELTA:-40}
LIVE_UPGRADE_MIN_LIVE_WORKERS=${PRL_LIVE_UPGRADE_MIN_LIVE_WORKERS:-9}
